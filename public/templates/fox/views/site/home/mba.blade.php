                        <li>Acquire a strong foundation in key functional areas of business management to enable them to succeed as effective managers/leaders in an increasingly complex and dynamic environment. </li>
                        <li>Acquire and use a range of concepts, tools and techniques for problem solving and decision-making for analyzing complex and inter-related business scenarios. </li>
